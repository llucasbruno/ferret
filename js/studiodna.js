// ── State ────────────────────────────────────
var dnaSelDoc = null;
var dnaAllDocs = [];
var dnaPendingSections = [];

// ── Render grid ──────────────────────────────
async function renderStudioDNA() {
  var isMgr = meData && meData.access === 'manager';

  var snap = await db.collection('studioDNA').orderBy('order', 'asc').get();
  var firestoreDocs = snap.docs.map(function(d) { return Object.assign({ id: d.id }, d.data()); });
  var firestoreIds = firestoreDocs.map(function(d) { return d.id; });
  var deletedIds = firestoreDocs.filter(function(d) { return d._deleted; }).map(function(d) { return d.id; });
  var realFirestore = firestoreDocs.filter(function(d) { return !d._deleted; });
  var defaults = DNA_DOCS_DEFAULT.filter(function(d) { return !firestoreIds.includes(d.id) && !deletedIds.includes(d.id); });
  dnaAllDocs = defaults.concat(realFirestore);

  $('dna-grid').innerHTML = dnaAllDocs.map(function(doc) {
    return '<div class="proj-card" onclick="openDNADoc(\'' + doc.id + '\')" style="cursor:pointer;">' +
      '<div style="position:absolute;top:0;left:0;right:0;height:3px;background:linear-gradient(90deg,' + doc.color + ',transparent);"></div>' +
      '<div class="proj-card-header">' +
        '<div style="font-size:22px;">' + doc.icon + '</div>' +
        '<div class="proj-name" style="color:var(--cream);font-size:16px;">' + doc.title + '</div>' +
      '</div>' +
      '<div style="font-family:var(--M);font-size:10px;color:var(--dim);letter-spacing:1px;margin-bottom:14px;">' + doc.subtitle + '</div>' +
      '<div style="display:flex;flex-direction:column;gap:5px;margin-bottom:16px;">' +
        (doc.sections || []).map(function(s) {
          return '<div style="display:flex;align-items:center;gap:6px;font-family:var(--M);font-size:10px;color:var(--dim);">' +
            '<span style="width:5px;height:5px;border-radius:50%;background:' + doc.color + ';flex-shrink:0;display:inline-block;"></span>' +
            s.heading +
          '</div>';
        }).join('') +
      '</div>' +
      '<div style="display:flex;align-items:center;justify-content:space-between;padding-top:10px;border-top:1px solid rgba(255,255,255,.06);">' +
        '<span style="font-family:var(--M);font-size:9px;color:var(--dim);letter-spacing:1px;">' + (doc.sections || []).length + ' SEÇÕES</span>' +
        '<span style="font-family:var(--M);font-size:9px;color:' + doc.color + ';letter-spacing:2px;">ABRIR →</span>' +
      '</div>' +
    '</div>';
  }).join('');
}

// ── Open doc ─────────────────────────────────
function openDNADoc(docId) {
  var doc = dnaAllDocs.find(function(d) { return d.id === docId; });
  if (!doc) return;
  dnaSelDoc = doc;
  var isMgr = meData && meData.access === 'manager';
  $('dna-modal-title').textContent = doc.title;
  $('dna-modal-icon').textContent  = doc.icon;
  $('dna-modal-sub').textContent   = doc.subtitle;
  $('dna-modal-body').innerHTML = (doc.sections || []).map(function(s) {
    return '<div style="margin-bottom:24px;">' +
      '<div style="font-family:var(--M);font-size:10px;color:var(--dim);letter-spacing:3px;text-transform:uppercase;margin-bottom:8px;display:flex;align-items:center;gap:8px;">' +
        '<span style="flex:1;height:1px;background:rgba(255,255,255,.07);"></span>' +
        s.heading +
        '<span style="flex:3;height:1px;background:rgba(255,255,255,.07);"></span>' +
      '</div>' +
      '<div style="font-size:14px;color:var(--cream);line-height:1.8;white-space:pre-wrap;">' + s.body + '</div>' +
    '</div>';
  }).join('');
  var actEl = $('dna-modal-acts');
  if (actEl) {
    actEl.innerHTML = isMgr
      ? '<button class="btn btn-danger btn-sm" onclick="deleteDNADoc(\'' + doc.id + '\',' + (doc.isDefault ? 'true' : 'false') + ')">🗑 EXCLUIR</button>'
      : '';
  }
  showModal('m-dna-doc');
}

// ── Create ────────────────────────────────────
function openCreateDNA() {
  dnaPendingSections = [];
  $('dna-title').value    = '';
  $('dna-subtitle').value = '';
  $('dna-icon').value     = '📌';
  $('dna-color').value    = 'var(--cyan)';
  $('dna-sec-heading').value = '';
  $('dna-sec-body').value    = '';
  _renderDNASectionsList();
  showModal('m-dna-create');
}

function addDNASection() {
  var h = $('dna-sec-heading').value.trim();
  var b = $('dna-sec-body').value.trim();
  if (!h || !b) { toast('Preencha título e conteúdo da seção', false); return; }
  dnaPendingSections.push({ heading: h, body: b });
  $('dna-sec-heading').value = '';
  $('dna-sec-body').value    = '';
  _renderDNASectionsList();
}

function removeDNASection(idx) {
  dnaPendingSections.splice(idx, 1);
  _renderDNASectionsList();
}

function _renderDNASectionsList() {
  var el = $('dna-sections-list');
  if (!el) return;
  if (!dnaPendingSections.length) {
    el.innerHTML = '<div style="font-family:var(--M);font-size:10px;color:var(--dim);padding:6px 0;">Nenhuma seção adicionada ainda.</div>';
    return;
  }
  el.innerHTML = dnaPendingSections.map(function(s, i) {
    return '<div style="display:flex;align-items:center;gap:8px;padding:8px 10px;background:rgba(255,255,255,.03);border-radius:2px;margin-bottom:4px;border-left:2px solid var(--cyan);">' +
      '<div style="flex:1;min-width:0;">' +
        '<div style="font-family:var(--R);font-size:13px;font-weight:600;color:var(--cream);">' + s.heading + '</div>' +
        '<div style="font-family:var(--M);font-size:10px;color:var(--dim);white-space:nowrap;overflow:hidden;text-overflow:ellipsis;">' + s.body.slice(0, 60) + (s.body.length > 60 ? '...' : '') + '</div>' +
      '</div>' +
      '<button onclick="removeDNASection(' + i + ')" style="background:none;border:none;color:var(--dim);cursor:pointer;font-size:16px;flex-shrink:0;">×</button>' +
    '</div>';
  }).join('');
}

async function saveDNADoc() {
  var title    = $('dna-title').value.trim();
  var subtitle = $('dna-subtitle').value.trim();
  var icon     = $('dna-icon').value.trim() || '📌';
  var color    = $('dna-color').value;
  if (!title)                    { toast('Informe o título do documento', false); return; }
  if (!dnaPendingSections.length) { toast('Adicione pelo menos uma seção', false); return; }
  var data = {
    title: title, subtitle: subtitle, icon: icon, color: color,
    sections: dnaPendingSections, order: Date.now(),
    createdById: me.uid, createdByName: meData.displayName,
    createdAt: firebase.firestore.FieldValue.serverTimestamp()
  };
  await db.collection('studioDNA').add(data);
  await log('project_create', meData.displayName + ' adicionou o documento "' + title + '" ao Studio DNA');
  toast('Documento "' + title + '" criado!', true);
  closeModal('m-dna-create');
  dnaPendingSections = [];
  await renderStudioDNA();
}

// ── Delete ────────────────────────────────────
async function deleteDNADoc(id, isDefault) {
  if (!confirm('Excluir este documento? Ação irreversível.')) return;
  var doc = dnaAllDocs.find(function(d) { return d.id === id; });
  if (isDefault) {
    await db.collection('studioDNA').doc(id).set({ _deleted: true, order: 9999 });
  } else {
    await db.collection('studioDNA').doc(id).delete();
  }
  await log('task_delete', meData.displayName + ' removeu o documento "' + (doc ? doc.title : '') + '" do Studio DNA');
  toast('Documento removido.', true);
  closeModal('m-dna-doc');
  await renderStudioDNA();
}
