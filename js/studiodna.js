// ── State ────────────────────────────────────
let dnaSelDoc = null;

// ── Render grid ──────────────────────────────
async function renderStudioDNA() {
  const isMgr = meData?.access === 'manager';

  // Busca docs customizados do Firestore (se existirem)
  const snap = await db.collection('studioDNA').orderBy('order', 'asc').get();
  const customDocs = snap.docs.map(d => ({ id: d.id, ...d.data() }));
  const allDocs = customDocs.length ? customDocs : DNA_DOCS_DEFAULT;

  $('dna-grid').innerHTML = allDocs.map(doc => `
    <div class="dna-card" onclick="openDNADoc('${doc.id}')" style="border-color:${doc.color}33;">
      <div style="position:absolute;top:0;left:0;right:0;height:3px;background:linear-gradient(90deg,${doc.color},transparent);"></div>
      <div class="dna-card-inner">
        <div class="dna-card-header">
          <div class="dna-card-icon" style="background:${doc.color}18;color:${doc.color};">${doc.icon}</div>
          <div class="dna-card-title" style="color:var(--cream)">${doc.title}</div>
        </div>
        <div class="dna-card-sub">${doc.subtitle}</div>
      </div>
      <div class="dna-card-footer">
        <span class="dna-card-sections">${doc.sections?.length || 0} SEÇÕES</span>
        <span class="dna-card-cta" style="color:${doc.color};">LER DOCUMENTO <span style="font-size:12px;">→</span></span>
      </div>
    </div>
  `).join('');
}

// ── Open doc modal ───────────────────────────
function openDNADoc(docId) {
  // Tenta no Firestore primeiro, senão usa default
  const snap_local = DNA_DOCS_DEFAULT.find(d => d.id === docId);
  if (!snap_local) return;
  dnaSelDoc = snap_local;
  _renderDNAModal(snap_local);
}

function _renderDNAModal(doc) {
  $('dna-modal-title').textContent = doc.title;
  $('dna-modal-icon').textContent  = doc.icon;
  $('dna-modal-sub').textContent   = doc.subtitle;
  $('dna-modal-body').innerHTML = doc.sections.map(s => `
    <div style="margin-bottom:24px;">
      <div style="font-family:var(--M);font-size:10px;color:var(--dim);letter-spacing:3px;text-transform:uppercase;margin-bottom:8px;display:flex;align-items:center;gap:8px;">
        <span style="flex:1;height:1px;background:rgba(255,255,255,.07);"></span>
        ${s.heading}
        <span style="flex:3;height:1px;background:rgba(255,255,255,.07);"></span>
      </div>
      <div style="font-size:14px;color:var(--cream);line-height:1.8;white-space:pre-wrap;">${s.body}</div>
    </div>
  `).join('');
  showModal('m-dna-doc');
}